package com.joyque.pojo;

public class LotteryInfo {

	private long lid;
	
	private long pid;
	
	private String imageUrl;
	
	private String bg1Url;
	
	private String bg2Url;
	
	private String bg3Url;
	
	private int credit;

	public long getLid() {
		return lid;
	}

	public void setLid(long lid) {
		this.lid = lid;
	}

	public long getPid() {
		return pid;
	}

	public void setPid(long pid) {
		this.pid = pid;
	}

	public String getImageUrl() {
		return imageUrl;
	}

	public void setImageUrl(String imageUrl) {
		this.imageUrl = imageUrl;
	}

	public String getBg1Url() {
		return bg1Url;
	}

	public void setBg1Url(String bg1Url) {
		this.bg1Url = bg1Url;
	}

	public String getBg2Url() {
		return bg2Url;
	}

	public void setBg2Url(String bg2Url) {
		this.bg2Url = bg2Url;
	}

	public String getBg3Url() {
		return bg3Url;
	}

	public void setBg3Url(String bg3Url) {
		this.bg3Url = bg3Url;
	}

	public int getCredit() {
		return credit;
	}

	public void setCredit(int credit) {
		this.credit = credit;
	}
}
